const LangContext = createContext({ lang: 'fr', setLang: () => {}, t: (k) => k })

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    if (typeof window === 'undefined') return 'fr'
    const saved = localStorage.getItem('tijan_lang')
    if (saved) return saved
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone || ''
    const enZones = ['Lagos', 'Accra', 'Nairobi', 'Johannesburg']
    return enZones.some(z => tz.includes(z)) ? 'en' : 'fr'
  })
  const setLang = (l) => { setLangState(l); localStorage.setItem('tijan_lang', l) }
  const t = (key) => { const e = T[key]; if (!e) return key; return e[lang] || e['fr'] || key }
  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>
}

export function useLang() { return useContext(LangContext) }

